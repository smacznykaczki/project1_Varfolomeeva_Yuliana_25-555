            print("–ù–µ–≤–µ—Ä–Ω—ã–π –æ—Ç–≤–µ—Ç –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–ª –ª–æ–≤—É—à–∫—É!")
