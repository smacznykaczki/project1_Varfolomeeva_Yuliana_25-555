def attempt_open_treasure(game_state):
    """
    –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–∫—Ä—ã—Ç–∏—è —Å—É–Ω–¥—É–∫–∞ —Å —Å–æ–∫—Ä–æ–≤–∏—â–∞–º–∏
    """
    current_room = game_state['current_room']
    room_data = ROOMS[current_room]
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –º—ã –≤ –∫–æ–º–Ω–∞—Ç–µ —Å —Å–æ–∫—Ä–æ–≤–∏—â–∞–º–∏ –∏ –µ—Å—Ç—å —Å—É–Ω–¥—É–∫
