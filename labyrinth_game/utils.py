        if answer == '–¥–∞':
            # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –∫–æ–¥
            code = get_puzzle_input("–í–≤–µ–¥–∏—Ç–µ –∫–æ–¥: ").strip()
            
