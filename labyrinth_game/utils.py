                print("–ù–µ–≤–µ—Ä–Ω—ã–π –∫–æ–¥. –°—É–Ω–¥—É–∫ –æ—Å—Ç–∞–µ—Ç—Å—è –∑–∞–ø–µ—Ä—Ç—ã–º.")
