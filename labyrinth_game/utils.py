def pseudo_random(seed, modulo):
    """
    –ü—Å–µ–≤–¥–æ—Å–ª—É—á–∞–π–Ω—ã–π –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä –Ω–∞ –æ—Å–Ω–æ–≤–µ —Å–∏–Ω—É—Å–∞
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ [0, modulo)
    """
    # –ë–µ—Ä–µ–º —Å–∏–Ω—É—Å –æ—Ç seed, —É–º–Ω–æ–∂–µ–Ω–Ω–æ–≥–æ –Ω–∞ –±–æ–ª—å—à–æ–µ —á–∏—Å–ª–æ —Å –¥—Ä–æ–±–Ω–æ–π —á–∞—Å—Ç—å—é
    sin_value = math.sin(seed * 12.9898)
    
    # –£–º–Ω–æ–∂–∞–µ–º –Ω–∞ –¥—Ä—É–≥–æ–µ –±–æ–ª—å—à–æ–µ —á–∏—Å–ª–æ —Å –¥—Ä–æ–±–Ω–æ–π —á–∞—Å—Ç—å—é
    multiplied_value = sin_value * 43758.5453
    
    # –ü–æ–ª—É—á–∞–µ–º –¥—Ä–æ–±–Ω—É—é —á–∞—Å—Ç—å
    fractional_part = multiplied_value - math.floor(multiplied_value)
    
    # –ü—Ä–∏–≤–æ–¥–∏–º –∫ –Ω—É–∂–Ω–æ–º—É –¥–∏–∞–ø–∞–∑–æ–Ω—É –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ
    result = math.floor(fractional_part * modulo)
    
    return result

def trigger_trap(game_state):
    """
    –§—É–Ω–∫—Ü–∏—è –¥–ª—è –∞–∫—Ç–∏–≤–∞—Ü–∏–∏ –ª–æ–≤—É—à–∫–∏ —Å –Ω–µ–≥–∞—Ç–∏–≤–Ω—ã–º–∏ –ø–æ—Å–ª–µ–¥—Å—Ç–≤–∏—è–º–∏ –¥–ª—è –∏–≥—Ä–æ–∫–∞
    """
    print("–õ–æ–≤—É—à–∫–∞ –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω–∞! –ü–æ–ª —Å—Ç–∞–ª –¥—Ä–æ–∂–∞—Ç—å...")
    
    inventory = game_state['player_inventory']
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —É –∏–≥—Ä–æ–∫–∞ –ø—Ä–µ–¥–º–µ—Ç—ã –≤ –∏–Ω–≤–µ–Ω—Ç–∞—Ä–µ
    if inventory:
        # –ò—Å–ø–æ–ª—å–∑—É–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —à–∞–≥–æ–≤ –∫–∞–∫ seed –¥–ª—è –ø—Å–µ–≤–¥–æ—Å–ª—É—á–∞–π–Ω–æ—Å—Ç–∏
        steps = game_state['steps_taken']
        # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π –∏–Ω–¥–µ–∫—Å –ø—Ä–µ–¥–º–µ—Ç–∞ –≤ –∏–Ω–≤–µ–Ω—Ç–∞—Ä–µ
        item_index = pseudo_random(steps, len(inventory))
        
        # –£–¥–∞–ª—è–µ–º –≤—ã–±—Ä–∞–Ω–Ω—ã–π –ø—Ä–µ–¥–º–µ—Ç
        lost_item = inventory.pop(item_index)
        
