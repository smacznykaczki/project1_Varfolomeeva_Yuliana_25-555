from .constants import ROOMS
 
def describe_current_room(game_state):
    """
    –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ–ø–∏—Å–∞–Ω–∏—è —Ç–µ–∫—É—â–µ–π –∫–æ–º–Ω–∞—Ç—ã
    """
    current_room_name = game_state['current_room']
    room = ROOMS[current_room_name]
    
    # –í—ã–≤–æ–¥–∏–º –Ω–∞–∑–≤–∞–Ω–∏–µ –∫–æ–º–Ω–∞—Ç—ã –≤ –≤–µ—Ä—Ö–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ
    print(f"\n== {current_room_name.upper()} ==")
    
    # –í—ã–≤–æ–¥–∏–º –æ–ø–∏—Å–∞–Ω–∏–µ –∫–æ–º–Ω–∞—Ç—ã
    print(room['description'])
    
    # –í—ã–≤–æ–¥–∏–º –∑–∞–º–µ—Ç–Ω—ã–µ –ø—Ä–µ–¥–º–µ—Ç—ã
    if room['items']:
        print("\n–ó–∞–º–µ—Ç–Ω—ã–µ –ø—Ä–µ–¥–º–µ—Ç—ã:")
        for item in room['items']:
            print(f"  - {item}")
    
    # –í—ã–≤–æ–¥–∏–º –¥–æ—Å—Ç—É–ø–Ω—ã–µ –≤—ã—Ö–æ–¥—ã
    if room['exits']:
        print("\n–í—ã—Ö–æ–¥—ã:")
        for direction, target_room in room['exits'].items():
            print(f"  - {direction}: {target_room}")
    
    # –°–æ–æ–±—â–µ–Ω–∏–µ –æ –Ω–∞–ª–∏—á–∏–∏ –∑–∞–≥–∞–¥–∫–∏
    if room['puzzle']:
        print("\n–ö–∞–∂–µ—Ç—Å—è, –∑–¥–µ—Å—å –µ—Å—Ç—å –∑–∞–≥–∞–¥–∫–∞ (–∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–æ–º–∞–Ω–¥—É solve).")
    
    print()  # –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ –¥–ª—è —á–∏—Ç–∞–±–µ–ª—å–Ω–æ—Å—Ç–∏


def get_puzzle_input(prompt="–í–∞—à –æ—Ç–≤–µ—Ç: "):
    """
    –û—Ç–¥–µ–ª—å–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –≤–≤–æ–¥–∞ –æ—Ç–≤–µ—Ç–æ–≤ –Ω–∞ –∑–∞–≥–∞–¥–∫–∏
    """
    try:
        return input(prompt)
    except (KeyboardInterrupt, EOFError):
        print("\n–í—ã –≤—ã—à–ª–∏ –∏–∑ —Ä–µ—à–µ–Ω–∏—è –∑–∞–≥–∞–¥–∫–∏.")
        return ""
 
def solve_puzzle(game_state):
    """
    –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Ä–µ—à–µ–Ω–∏—è –∑–∞–≥–∞–¥–æ–∫ –≤ —Ç–µ–∫—É—â–µ–π –∫–æ–º–Ω–∞—Ç–µ
    """
    current_room = game_state['current_room']
    room_data = ROOMS[current_room]
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –∑–∞–≥–∞–¥–∫–∞ –≤ —Ç–µ–∫—É—â–µ–π –∫–æ–º–Ω–∞—Ç–µ
    if not room_data['puzzle']:
        print("–ó–∞–≥–∞–¥–æ–∫ –∑–¥–µ—Å—å –Ω–µ—Ç.")
        return False
    
    # –ü–æ–ª—É—á–∞–µ–º –≤–æ–ø—Ä–æ—Å –∏ –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç
    question, correct_answer = room_data['puzzle']
    
    # –í—ã–≤–æ–¥–∏–º –≤–æ–ø—Ä–æ—Å
    print(f"\n–ó–∞–≥–∞–¥–∫–∞: {question}")
    
    # –ü–æ–ª—É—á–∞–µ–º –æ—Ç–≤–µ—Ç –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–µ—Ä–µ–∑ –æ—Ç–¥–µ–ª—å–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é
    user_answer = get_puzzle_input("–í–∞—à –æ—Ç–≤–µ—Ç: ").strip()
    
    # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∏—á–µ–≥–æ –Ω–µ –≤–≤–µ–ª
    if not user_answer:
        print("–í—ã –Ω–µ –≤–≤–µ–ª–∏ –æ—Ç–≤–µ—Ç.")
        return False
    
    # –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º –æ—Ç–≤–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å –ø—Ä–∞–≤–∏–ª—å–Ω—ã–º –æ—Ç–≤–µ—Ç–æ–º
    if user_answer.lower() == correct_answer.lower():
        # –ï—Å–ª–∏ –æ—Ç–≤–µ—Ç –≤–µ—Ä–Ω—ã–π
        print("–ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º! –ó–∞–≥–∞–¥–∫–∞ —Ä–∞–∑–≥–∞–¥–∞–Ω–∞! üéâ")
        
        # –£–±–∏—Ä–∞–µ–º –∑–∞–≥–∞–¥–∫—É –∏–∑ –∫–æ–º–Ω–∞—Ç—ã
        room_data['puzzle'] = None
        
        # –î–æ–±–∞–≤–ª—è–µ–º –∏–≥—Ä–æ–∫—É –Ω–∞–≥—Ä–∞–¥—É (–∑–æ–ª–æ—Ç—ã–µ –º–æ–Ω–µ—Ç—ã)
        if 'gold_coins' not in game_state['player_inventory']:
            game_state['player_inventory'].append('gold_coins')
            print("–í—ã –ø–æ–ª—É—á–∏–ª–∏ –Ω–∞–≥—Ä–∞–¥—É: –∑–æ–ª–æ—Ç—ã–µ –º–æ–Ω–µ—Ç—ã! üí∞")
        
        return True
    else:
        # –ï—Å–ª–∏ –æ—Ç–≤–µ—Ç –Ω–µ–≤–µ—Ä–Ω—ã–π
        print("–ù–µ–≤–µ—Ä–Ω–æ. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞.")
        return False
    
def attempt_open_treasure(game_state):
    """
    –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–∫—Ä—ã—Ç–∏—è —Å—É–Ω–¥—É–∫–∞ —Å —Å–æ–∫—Ä–æ–≤–∏—â–∞–º–∏
    """
    current_room = game_state['current_room']
    room_data = ROOMS[current_room]
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –º—ã –≤ –∫–æ–º–Ω–∞—Ç–µ —Å —Å–æ–∫—Ä–æ–≤–∏—â–∞–º–∏ –∏ –µ—Å—Ç—å —Å—É–Ω–¥—É–∫
    if current_room != 'treasure_room' or 'treasure_chest' not in room_data['items']:
        return False
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–ª—é—á–∞
    if 'treasure_key' in game_state['player_inventory']:
        # –ï—Å–ª–∏ –∫–ª—é—á –µ—Å—Ç—å
        print("–í—ã –ø—Ä–∏–º–µ–Ω—è–µ—Ç–µ –∫–ª—é—á, –∏ –∑–∞–º–æ–∫ —â—ë–ª–∫–∞–µ—Ç. –°—É–Ω–¥—É–∫ –æ—Ç–∫—Ä—ã—Ç!")
        
        # –£–¥–∞–ª—è–µ–º —Å—É–Ω–¥—É–∫ –∏–∑ –∫–æ–º–Ω–∞—Ç—ã
        room_data['items'].remove('treasure_chest')
        
        # –°–æ–æ–±—â–∞–µ–º –æ –ø–æ–±–µ–¥–µ
        print("–í —Å—É–Ω–¥—É–∫–µ —Å–æ–∫—Ä–æ–≤–∏—â–µ! –í—ã –ø–æ–±–µ–¥–∏–ª–∏! üèÜ")
        game_state['game_over'] = True
        return True
    
    else:
        # –ï—Å–ª–∏ –∫–ª—é—á–∞ –Ω–µ—Ç, –ø—Ä–µ–¥–ª–∞–≥–∞–µ–º –≤–≤–µ—Å—Ç–∏ –∫–æ–¥
        print("–°—É–Ω–¥—É–∫ –∑–∞–ø–µ—Ä—Ç. –ù–∞ –∑–∞–º–∫–µ –µ—Å—Ç—å –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –¥–ª—è –≤–≤–æ–¥–∞ –∫–æ–¥–∞.")
        answer = get_puzzle_input("–í–≤–µ—Å—Ç–∏ –∫–æ–¥? (–¥–∞/–Ω–µ—Ç): ").strip().lower()
        
        if answer == '–¥–∞':
            # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –∫–æ–¥
            code = get_puzzle_input("–í–≤–µ–¥–∏—Ç–µ –∫–æ–¥: ").strip()
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ–¥ —Å –∑–∞–≥–∞–¥–∫–æ–π –∫–æ–º–Ω–∞—Ç—ã
            if room_data['puzzle'] and code == room_data['puzzle'][1]:
                # –ï—Å–ª–∏ –∫–æ–¥ –≤–µ—Ä–Ω—ã–π
                print("–ó–∞–º–æ–∫ —â—ë–ª–∫–∞–µ—Ç! –°—É–Ω–¥—É–∫ –æ—Ç–∫—Ä—ã—Ç!")
                
                # –£–¥–∞–ª—è–µ–º —Å—É–Ω–¥—É–∫ –∏–∑ –∫–æ–º–Ω–∞—Ç—ã
                room_data['items'].remove('treasure_chest')
                
                # –°–æ–æ–±—â–∞–µ–º –æ –ø–æ–±–µ–¥–µ
                print("–í —Å—É–Ω–¥—É–∫–µ —Å–æ–∫—Ä–æ–≤–∏—â–µ! –í—ã –ø–æ–±–µ–¥–∏–ª–∏! üèÜ")
                game_state['game_over'] = True
                return True
            else:
                # –ï—Å–ª–∏ –∫–æ–¥ –Ω–µ–≤–µ—Ä–Ω—ã–π
                print("–ù–µ–≤–µ—Ä–Ω—ã–π –∫–æ–¥. –°—É–Ω–¥—É–∫ –æ—Å—Ç–∞–µ—Ç—Å—è –∑–∞–ø–µ—Ä—Ç—ã–º.")
                return False
        else:
            # –ï—Å–ª–∏ –∏–≥—Ä–æ–∫ –æ—Ç–∫–∞–∑–∞–ª—Å—è –≤–≤–æ–¥–∏—Ç—å –∫–æ–¥
            print("–í—ã –æ—Ç—Å—Ç—É–ø–∞–µ—Ç–µ –æ—Ç —Å—É–Ω–¥—É–∫–∞.")
            return False